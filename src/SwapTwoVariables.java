public class SwapTwoVariables {
    public static void main(String[] args) {
        int a = 5;
        int b = 13;
        int c = 0;

        c = a;
        a = b;
        b = c;
        System.out.println(" a = " + a);
        System.out.println(" b = " + b);
    }

}


